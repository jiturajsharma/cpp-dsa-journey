#include<iostream>
using namespace std;

int main(){

    // all about char and ASCII value
    char ch = 'A';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = 'a';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = '0';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = '9';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = ' ';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = '$';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl; 
    ch = '\n';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    ch = '\t';
    cout<<"Character: "<<ch<<endl;
    cout<<"ASCII value of "<<ch<<" is "<<int(ch)<<endl;
    
}