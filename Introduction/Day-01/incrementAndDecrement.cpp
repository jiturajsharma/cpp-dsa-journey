#include<iostream>
using namespace std;   

int main(){
    // increment and decrement
    // this is a comment
    int x = 5;
    x++; // post increment
    cout<<x<<endl;
    ++x;  // pre increment  -> x = x +1
    cout<<x<<endl;
    --x;
    cout<<x<<endl;

    cout<<"*********************************"<<endl;

    int y = 5;
    cout<<y<<endl;
    cout<<--y<<endl;
    cout<<y--<<endl;
    cout<<y;
}